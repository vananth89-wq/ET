CREATE TABLE employee_dependents (
  id                  UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  dependent_code      TEXT        NOT NULL,           -- e.g. EMP1001_DEP_01, generated by trigger
  employee_id         UUID        NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  relationship_type   TEXT        NOT NULL,           -- ref_id from DEPENDENT_RELATIONSHIP_TYPE
  dependent_name      TEXT        NOT NULL,
  date_of_birth       DATE        NOT NULL,
  gender              TEXT        NOT NULL,           -- 'Male' | 'Female'
  insurance_eligible  BOOLEAN     NOT NULL DEFAULT false,
  effective_from      DATE        NOT NULL,
  effective_to        DATE        NOT NULL DEFAULT '9999-12-31',
  is_active           BOOLEAN     NOT NULL DEFAULT true,
  inactive_at         TIMESTAMPTZ,
  inactive_by         UUID        REFERENCES profiles(id),
  created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_by          UUID        NOT NULL REFERENCES profiles(id),
  updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_by          UUID        NOT NULL REFERENCES profiles(id),

  CONSTRAINT chk_dep_effective_order   CHECK (effective_to >= effective_from),
  CONSTRAINT chk_dep_dob_not_future    CHECK (date_of_birth <= CURRENT_DATE),
  CONSTRAINT chk_dep_name_not_blank    CHECK (length(trim(dependent_name)) > 0),
  CONSTRAINT chk_dep_gender_valid      CHECK (gender IN ('Male','Female'))
);

COMMENT ON TABLE employee_dependents IS
  'Effective-dated dependent records for employees. '
  'Active record = is_active=true AND effective_to=''9999-12-31''. '
  'All versions (history + active) of the same dependent share a dependent_code. '
  'Terminal (removal) record = is_active=false AND effective_to=''9999-12-31''. '
  'dependent_code format: {employee_code}_DEP_{NN} e.g. EMP1001_DEP_01. '
  'Gender stored as plain text ''Male'' | ''Female''. '
  'Partial unique index uix_dependent_code_active enforces one open-ended row per dependent_code. '
  'Mig 289: initial creation.';

-- ── Indexes ───────────────────────────────────────────────────────────────────

-- Fast employee lookups (primary query pattern)
CREATE INDEX idx_dependents_employee
  ON employee_dependents(employee_id);

-- Lookup by dependent_code (amendment, removal, attachment join)
CREATE INDEX idx_dependents_code
  ON employee_dependents(dependent_code);

-- Active dependents per employee (most frequent query pattern)
CREATE INDEX idx_dependents_active
  ON employee_dependents(employee_id, is_active)
  WHERE is_active = true AND effective_to = '9999-12-31';

-- Timeline queries (history view ordered by period)
CREATE INDEX idx_dependents_timeline
  ON employee_dependents(dependent_code, effective_from, effective_to);

-- ── Partial unique index ──────────────────────────────────────────────────────

-- Enforces exactly one open-ended row per dependent_code at any time.
-- Whether the open-ended row is active (is_active=true) or terminal (is_active=false),
-- there can only ever be one row with effective_to='9999-12-31' per dependent_code.
CREATE UNIQUE INDEX uix_dependent_code_active
  ON employee_dependents(dependent_code)
  WHERE effective_to = '9999-12-31';


-- =============================================================================
-- Part 5 — fn_generate_dependent_code() trigger function + triggers
-- =============================================================================

CREATE OR REPLACE FUNCTION fn_generate_dependent_code()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  v_emp_code TEXT;
  v_seq      INT;
BEGIN
  IF TG_OP = 'INSERT' AND (NEW.dependent_code IS NULL OR NEW.dependent_code = '') THEN
    -- Advisory lock per employee to prevent race conditions on concurrent inserts
    PERFORM pg_advisory_xact_lock(hashtext(NEW.employee_id::TEXT));

    -- Get the employee's code (stored as employee_id column in employees table)
    SELECT employee_id INTO v_emp_code
    FROM employees
    WHERE id = NEW.employee_id;

    -- Determine next sequence number across ALL rows for this employee
    -- (active + historical + terminal, so codes never collide)
    SELECT COALESCE(MAX(
      (regexp_match(dependent_code, '_DEP_(\d+)$'))[1]::INT
    ), 0) + 1
    INTO v_seq
    FROM employee_dependents
    WHERE employee_id = NEW.employee_id;

    NEW.dependent_code := v_emp_code || '_DEP_' || LPAD(v_seq::TEXT, 2, '0');
  END IF;
  RETURN NEW;
END;
$$;

COMMENT ON FUNCTION fn_generate_dependent_code() IS
  'BEFORE INSERT trigger on employee_dependents. '
  'Auto-generates dependent_code = {employee_code}_DEP_{NN} when not supplied. '
  'Uses pg_advisory_xact_lock to prevent duplicate codes under concurrent inserts. '
  'Mig 289: initial creation.';

-- Apply code-generation trigger (BEFORE INSERT)
CREATE TRIGGER trg_dependent_code_generate
  BEFORE INSERT ON employee_dependents
  FOR EACH ROW EXECUTE FUNCTION fn_generate_dependent_code();

-- Apply updated_at maintenance trigger (BEFORE UPDATE)
CREATE TRIGGER trg_employee_dependents_updated_at
  BEFORE UPDATE ON employee_dependents
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- Apply audit trigger (AFTER INSERT/UPDATE/DELETE)
CREATE TRIGGER trg_employee_dependents_audit
  AFTER INSERT OR UPDATE OR DELETE ON employee_dependents
  FOR EACH ROW EXECUTE FUNCTION trg_write_audit_log();


