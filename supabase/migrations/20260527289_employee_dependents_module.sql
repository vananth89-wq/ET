-- Part 11 — get_employee_dependents RPC
-- =============================================================================

CREATE OR REPLACE FUNCTION get_employee_dependents(
  p_employee_id      uuid,
  p_include_history  boolean DEFAULT false
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_result jsonb;
BEGIN
  -- ── Access guard ──────────────────────────────────────────────────────────
  -- Four access paths:
  --   a) HR/admin with dependents.view permission scoped to employee's target group
  --   b) HR/admin with dependents.edit permission (can view as part of edit access)
  --   c) Employee viewing their own dependents with ESS dependents.view permission
  --   d) Approver holding a pending workflow task for this employee
  --   e) Initiator whose submission was sent back for clarification
  IF NOT (
    user_can('dependents', 'view', p_employee_id)
    OR user_can('dependents', 'edit', p_employee_id)
    OR (
      p_employee_id = get_my_employee_id()
      AND has_permission('dependents.view')
    )
    OR EXISTS (
      SELECT 1
      FROM   workflow_tasks wt
      JOIN   workflow_instances wi ON wi.id = wt.instance_id
      WHERE  wi.record_id   = p_employee_id
        AND  wt.assigned_to = auth.uid()
        AND  wt.status      = 'pending'
    )
    OR EXISTS (
      SELECT 1
      FROM   workflow_instances wi
      WHERE  wi.record_id    = p_employee_id
        AND  wi.submitted_by = auth.uid()
        AND  wi.status       = 'awaiting_clarification'
    )
  ) THEN
    RETURN '[]'::jsonb;
  END IF;

  -- ── Query ──────────────────────────────────────────────────────────────────
  SELECT jsonb_agg(
    jsonb_build_object(
      'id',                  d.id,
      'dependent_code',      d.dependent_code,
      'employee_id',         d.employee_id,
      'relationship_type',   d.relationship_type,
      'dependent_name',      d.dependent_name,
      'date_of_birth',       d.date_of_birth,
      'gender',              d.gender,
      'insurance_eligible',  d.insurance_eligible,
      'effective_from',      d.effective_from,
      'effective_to',        d.effective_to,
      'is_active',           d.is_active,
      'inactive_at',         d.inactive_at,
      'inactive_by',         d.inactive_by,
      'created_at',          d.created_at,
      'created_by',          d.created_by,
      'updated_at',          d.updated_at,
      'updated_by',          d.updated_by,
      'attachments', COALESCE(
        (SELECT jsonb_agg(jsonb_build_object(
          'id',                 att.id,
          'document_type',      att.document_type,
          'file_name',          att.file_name,
          'original_file_name', att.original_file_name,
          'file_path',          att.file_path,
          'mime_type',          att.mime_type,
          'file_size',          att.file_size,
          'uploaded_at',        att.uploaded_at
        ) ORDER BY att.uploaded_at)
        FROM employee_dependent_attachments att
        WHERE att.dependent_code = d.dependent_code
          AND att.employee_id    = d.employee_id
          AND att.is_active      = true),
        '[]'::jsonb
      )
    )
    ORDER BY d.dependent_code, d.effective_from DESC
  )
  INTO v_result
  FROM employee_dependents d
  WHERE d.employee_id = p_employee_id
    AND (
      p_include_history = true
      OR (d.is_active = true AND d.effective_to = '9999-12-31'::date)
    );

  RETURN COALESCE(v_result, '[]'::jsonb);
END;
$$;

COMMENT ON FUNCTION get_employee_dependents(uuid, boolean) IS
  'Returns dependent records for an employee. '
  'Pass p_include_history=true for full effective-dated history including removed dependents. '
  'Default (p_include_history=false) returns only currently active open-ended rows. '
  'Each record includes its active attachments. '
  'Access-guarded: caller must have view or edit dependents permission scoped to the employee, '
  'or be the employee themselves, or hold a pending workflow task, or be the initiator of a sent-back submission. '
  'Returns []::jsonb (not an error) when access is denied. '
  'Mig 289: initial creation.';

REVOKE ALL     ON FUNCTION get_employee_dependents(uuid, boolean) FROM PUBLIC;
GRANT  EXECUTE ON FUNCTION get_employee_dependents(uuid, boolean) TO authenticated;


